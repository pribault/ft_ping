#include "libft.h"

int		ft_sqrt(int n)
{
	int		i;

	if (n <= 0)
		return (0);
	i = 1;
	while (i * i <= n)
		i++;
	if (i * i - n < n - (i + 1) * (i + 1))
		return (i);
	return (i - 1);
}
